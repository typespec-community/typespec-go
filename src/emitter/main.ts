export { mapTypeSpecToGo };

function mapTypeSpecToGo(type: Type): string {
  // Use type guards with proper format handling
  if (isScalarType(type)) {
    return mapScalarToGo(type);
  }
  
  if (isModelType(type)) {
    return mapModelToGo(type);
  }
  
  if (isUnionType(type)) {
    return mapUnionToGo(type);
  }
  
  if (isEnumType(type)) {
    return mapEnumToGo(type);
  }
  
  // Handle builtin primitive types
  if (type.kind === "String") {
    return "string";
  }
  
  if (type.kind === "Boolean") {
    return "bool";
  }
  
  // Handle numeric types from test data
  const numericTypeMap: Record<string, string> = {
    "Int8": "int8",
    "Int16": "int16", 
    "Int32": "int32",
    "Int64": "int64",
    "Uint8": "uint8",
    "Uint16": "uint16",
    "Uint32": "uint32", 
    "Uint64": "uint64",
    "Float32": "float32",
    "Float64": "float64"
  };
  
  const kind = type.kind;
  if (numericTypeMap[kind]) {
    return numericTypeMap[kind];
  }
  
  // Handle Array pattern using TypeSpec compiler types
  if (isModelType(type) && isArrayModel(type)) {
    const elementType = getArrayElementType(type);
    if (elementType) {
      const elementGoType = mapTypeSpecToGo(elementType);
      return `[]${elementGoType}`;
    }
  }
  
  // Handle scalar types properly
  if (isScalarType(type)) {
    return mapScalarToGo(type);
  }
  
  // Last resort: return interface{} instead of any for better type safety
  Logger.warn(
    LogContext.TYPESPEC_INTEGRATION,
    `Unhandled type kind: ${type.kind}, falling back to interface{}`,
    { typeName: TypeSpecTypeSafeAccess.getTypeName(type) }
  );
  return "interface{}";
}

function mapScalarToGo(scalar: Scalar): string {
  const scalarName = getScalarName(scalar).toLowerCase();
  
  // Map builtin scalars with type safety
  const scalarMap: Record<string, string> = {
    "string": "string",
    "boolean": "bool",
    "int8": "int8",
    "int16": "int16",
    "int32": "int32",
    "int64": "int64",
    "uint8": "uint8",
    "uint16": "uint16",
    "uint32": "uint32",
    "uint64": "uint64",
    "float32": "float32",
    "float64": "float64",
    "bytes": "[]byte",
    "plaindate": "time.Time",
    "plaintime": "time.Time",
    "utcdatetime": "time.Time",
    "offsetdatetime": "time.Time",
    "duration": "time.Duration",
    "url": "string",
    "null": "interface{}"
  };
  
  const goType = scalarMap[scalarName];
  if (goType) {
    return goType;
  }
  
  Logger.warn(
    LogContext.TYPESPEC_INTEGRATION,
    `Unknown scalar type: ${scalarName}, using interface{} for type safety`,
    { scalarName }
  );
  return "interface{}";
}

function mapModelToGo(model: Model): string {
  const modelName = getModelName(model);
  
  // Handle array models (TypeSpec creates Model for arrays)
  if (isArrayModel(model) && hasIndexer(model)) {
    const elementType = getArrayElementType(model);
    if (elementType) {
      const elementGoType = mapTypeSpecToGo(elementType);
      return `[]${elementGoType}`;
    }
  }
  
  // Regular model - return by name
  if (modelName && modelName !== "Array") {
    return modelName;
  }
  
  Logger.warn(
    LogContext.TYPESPEC_INTEGRATION,
    `Model without name detected, using any`,
    { modelName }
  );
  return "any";
}

